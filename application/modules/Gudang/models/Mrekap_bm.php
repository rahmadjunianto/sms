    // datatables
    function json() {
        $bulan=$this->session->userdata('bulan');
        $tahun=$this->session->userdata('tahun');
        $this->datatables->select("DATE_FORMAT(tanggal, '%d-%m-%Y') as tgl,kd_barang_masuk,tanggal,no_ttb,no_spp,purchase,c.inisial,a.kd_barang,a.nm_barang,b.spesifikasi,a.jumlah,b.satuan,a.harga,(a.jumlah*a.harga) AS total");
        $this->datatables->from('tr_barang_masuk a');
        $this->datatables->join('ref_barang b', 'a.kd_barang=b.kd_barang');
        $this->datatables->join('ref_kategori c', 'b.kd_kategori=c.kd_kategori');
        $this->datatables->where("YEAR(a.tanggal)='$tahun' AND MONTH(a.tanggal)='$bulan'");
        $this->db->order_by("tanggal", "asc");

        return $this->datatables->generate();
    }
 
