    function getlap($tahun,$bulan){
        return $this->db->query("SELECT DATE_FORMAT(tanggal, '%d-%m-%Y') as tgl,kd_barang_masuk,tanggal,no_ttb,no_spp,purchase,c.inisial,a.kd_barang,a.nm_barang,b.spesifikasi,a.jumlah,b.satuan,a.harga,a.jumlah*a.harga AS total
FROM tr_barang_masuk a JOIN ref_barang b ON a.kd_barang=b.kd_barang
